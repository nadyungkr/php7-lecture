<?php

/**
 * 문자 치환하기
 */
str_replace("Hello", "Hi", "Hello, world");

/*
 * 문자 반복하기
 */
str_repeat("*", 5);

/*
 * 문자열 분리하기
 */
str_split("Hello, world", 3);

/*
 * 단어 헤아리기
 */
str_word_count("Hello, world", 1);

/*
 * 문자열 비교하기
 */
strcmp("Hello, world", "Hello, world");

/*
 * 문자의 위치 찾기
 */
strpos("Hello, world", "e");

/*
 * 하나라도 포함하는 문자열의 위치 찾기
 */
strpbrk("Hello, world", "l");

/*
 * 문자열의 길이 구하기
 */
strlen("Hello, world");

/*
 * 문자열 뒤집기
 */
strrev("Hello, world");
